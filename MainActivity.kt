package com.practicum.volume

fun main() {
    val length: Int = 9     //длина
    val width: Int = 6      //ширина
    val height: Int = 3     //высота
    val volume: Int = (length * width * height)
    /* формула расчёта объёма
    Перемножение всех значений */
    println(volume)     //результат 162
}